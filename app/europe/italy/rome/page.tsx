import { Metadata } from 'next'
import Navbar from '@/components/shared/Navbar'
import Footer from '@/components/shared/Footer'
import CityPageLayout from '@/components/Europe/cities/CityPageLayout'
import { getCityDetails } from '@/components/Europe/cities/cityDetailsData'

export const metadata: Metadata = {
  title: 'Rome Travel Guide | Trawel',
  description: 'Explore Rome - the Eternal City. Discover ancient ruins, Vatican treasures, world-class cuisine, and vibrant nightlife.',
}

export default function RomePage() {
  const cityData = getCityDetails('rome')
  
  if (!cityData) {
    return <div>City not found</div>
  }

  return (
    <>
      <Navbar />
      <div className="pt-16 sm:pt-20">
        <CityPageLayout city={cityData} />
      </div>
      <Footer />
    </>
  )
}
